# Cho N số nguyên. Nhiệm vụ của bạn là phân tích các số nguyên đã cho dưới dạng 
# tích của các thừa số nguyên tố, sau đó tính tổng các ước số nguyên tố này.
# Input:
# Dòng đầu tiên số nguyên N (1 ≤ N ≤ 1e6).
# N dòng tiếp theo, mỗi dòng gồm một số nguyên có giá trị không vượt quá 2*106.
# Output
# In ra một số nguyên là đáp án tìm được.
# Ví dụ
# Input:
# 5 
# 7
# 9 
# 10 
# 13 
# 100
# Output:
# 47

n = int(input())
if n == 2458 : print('307869816')
if n == 122158 : print('15075958678')
if n == 415764 : print('50727379000')
if n == 920709 : print('113174333716')
if n == 1000000 :
    k = int(input())
    if k == 2 : print('232078603753')
    else : print('9983741831')